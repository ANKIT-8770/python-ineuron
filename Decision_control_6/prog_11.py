"""
Write a python script to take the month value in numeric format and display the
number of days in it.

"""




mon=int(input("enter month value in numeric format : "))

if mon==1:
    print("january")
elif mon==2:
    print("february")
elif mon==3:
    print("march")
elif mon==4:
    print("April")
elif mon==5:
    print("May")
elif mon==6:
    print("June")
elif mon==7:
    print("July")
elif mon==8:
    print("August")
elif mon==9:
    print("September")
elif mon==10:
    print("october")
elif mon==11:
    print("November")
elif mon==12:
    print("December")
else:
    print("invalid input")
